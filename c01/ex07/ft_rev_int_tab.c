#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	r;

	i = 0;
	while (i < size / 2)
	{
		r = tab[size - 1 - i];
		tab[size - 1 - i] = tab[i];
		tab[i] = r;
		i++;
	}
}
